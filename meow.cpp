#include <iostream> // Required for input and output
using namespace std;

int main()
{
    // Declaring and initializing variables
    int myAge = 20;             // Integer
    float pi = 3.14f;           // Float
    double largeNumber = 2.718; // Double
    char grade = 'A';           // Character
    bool isCodingFun = true;    // Boolean

    // Printing the variables
    cout << "My age is: " << myAge << endl;
    cout << "Value of Pi: " << pi << endl;
    cout << "A larger number: " << largeNumber << endl;
    cout << "Grade in C++ class: " << grade << endl;
    cout << "Is coding fun? " << isCodingFun << endl;

    return 0;
}
